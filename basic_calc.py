num_1 = int(input('Enter the first number: '))
num_2 = int(input('Enter the second number: '))

#addition
print('Addition of {} + {} = '.format(num_1,num_2))
print(num_1 + num_2)

#subtraction
print('Subtraction of {} - {} = '.format(num_1,num_2))
print(num_1 - num_2)

#multiplication
print('Multiplication of {} * {} = '.format(num_1,num_2))
print(num_1 * num_2)

#division
print('Division of {} / {} = '.format(num_1,num_2))
print(num_1 / num_2)

#floor division
print('Floor division of {} // {} = '.format(num_1,num_2))
print(num_1 // num_2)

#modulo
print('Modulus of {} % {} = '.format(num_1,num_2))
print(num_1 % num_2)

